        public static bool GetMimeType(string fileName, out string output)
        {
            output = string.Empty;
            try
            {
                string fileExtension = Path.GetExtension(fileName).Substring(1).ToLower();
                string contentType = MimeTypes[fileExtension];
                output = contentType != null ? contentType : string.Empty;
                return true;
            }
            catch (KeyNotFoundException)
            {
                return false;
            }
            catch
            {
                throw;
            }
        }
